package com.mycompany.groupMsg.dto;

public class GroupDto {

	private String serial;
	private String memberSerial;
	private String name;
	private String purpose;
	private String numMember;
	
	public String getSerial() {
		return serial;
	}
	public void setSerial(String serial) {
		this.serial = serial;
	}
	public String getMemberSerial() {
		return memberSerial;
	}
	public void setMemberSerial(String memberSerial) {
		this.memberSerial = memberSerial;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPurpose() {
		return purpose;
	}
	public void setPurpose(String purpose) {
		this.purpose = purpose;
	}
	public String getNumMember() {
		return numMember;
	}
	public void setNumMember(String numMember) {
		this.numMember = numMember;
	}
	
}
